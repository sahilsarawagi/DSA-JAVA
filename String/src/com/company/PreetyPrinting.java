package com.company;

public class PreetyPrinting {
    public static void main(String[] args) {
        float a = 45.65952f;
        System.out.printf("Print to the 2 decimal number %.2f", a);
        System.out.println();
        System.out.printf("PI:  %.3f", Math.PI);
        System.out.println();
        String b = "Uchiha";
        System.out.printf("Hello my name is %s and %s is my surname.", "sasuke", b);

    }
}

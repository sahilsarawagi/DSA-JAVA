package com.company;

public class Loops {
    public static void main(String[] args) {

        // for loop
        for (int i = 1; i <5 ;i++){
            System.out.println(i);
        }
        // while loop
        int num=1;
        while (num<5){
            System.out.println(num);
            num++;
        }
        num=1;

        // do while loop
        do {
            System.out.println(num);
            num++;
        }while (num<5);

    }

}
